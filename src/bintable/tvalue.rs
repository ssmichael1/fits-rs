#[derive(Clone, Debug)]
pub enum BinTableValue {
    Logical(bool),
    Bit(bool),
    UnsignedByte(u8),
    Int16(i16),
    Int32(i32),
    Int64(i64),
    Char(char),
    Float32(f32),
    Float64(f64),
    Complex32((f32, f32)),
    Complex64((f64, f64)),
    LogicalArr(Vec<bool>),
    BitArr(Vec<bool>),
    UnsignedByteArr(Vec<u8>),
    Int16Arr(Vec<i16>),
    Int32Arr(Vec<i32>),
    Int64Arr(Vec<i64>),
    String(String),
    Float32Arr(Vec<f32>),
    Float64Arr(Vec<f64>),
    Complex32Arr(Vec<(f32, f32)>),
    Complex64Arr(Vec<(f64, f64)>),
    Array(Box<Vec<BinTableValue>>),
}
